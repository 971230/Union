package com.zte.cbss.autoprocess.model;

public class SimcardInfo {
	private String resKindCode;
	private String xoption;
	private String emptyCardId;
	private String procid;
	private String imsi;
	private String capacityTypeCode;
	private String cardData;
	
	public String getResKindCode() {
		return resKindCode;
	}
	public void setResKindCode(String resKindCode) {
		this.resKindCode = resKindCode;
	}
	public String getXoption() {
		return xoption;
	}
	public void setXoption(String xoption) {
		this.xoption = xoption;
	}
	public String getProcid() {
		return procid;
	}
	public void setProcid(String procid) {
		this.procid = procid;
	}
	public String getImsi() {
		return imsi;
	}
	public void setImsi(String imsi) {
		this.imsi = imsi;
	}
	public String getCapacityTypeCode() {
		return capacityTypeCode;
	}
	public void setCapacityTypeCode(String capacityTypeCode) {
		this.capacityTypeCode = capacityTypeCode;
	}
	public String getCardData() {
		return cardData;
	}
	public void setCardData(String cardData) {
		this.cardData = cardData;
	}
	public String getEmptyCardId() {
		return emptyCardId;
	}
	public void setEmptyCardId(String emptyCardId) {
		this.emptyCardId = emptyCardId;
	}
	}
