package com.ztesoft.net.mall.core.model;

public class GoodsDetail implements java.io.Serializable{
	//电脑
	private String series;
	private String model;//型号
	private String color;
	private String platform;
	private String os;
	private String cpu_style;
	private String cpu_model;
	private String cpu_speed;
	public String getSeries() {
		return series;
	}
	public void setSeries(String series) {
		this.series = series;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getPlatform() {
		return platform;
	}
	public void setPlatform(String platform) {
		this.platform = platform;
	}
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	public String getCpu_style() {
		return cpu_style;
	}
	public void setCpu_style(String cpu_style) {
		this.cpu_style = cpu_style;
	}
	public String getCpu_model() {
		return cpu_model;
	}
	public void setCpu_model(String cpu_model) {
		this.cpu_model = cpu_model;
	}
	public String getCpu_speed() {
		return cpu_speed;
	}
	public void setCpu_speed(String cpu_speed) {
		this.cpu_speed = cpu_speed;
	}
	

}