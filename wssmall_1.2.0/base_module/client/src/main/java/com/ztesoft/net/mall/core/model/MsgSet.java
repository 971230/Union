public class MsgSet  implements java.io.Serializable {


	 private String set_id;  //短信模板设置标识
     private String set_type;  //模板类型（适用范围，比如用于抓取订单号码时，校验号码有问题需要发送短信）
     private String receiver;  //接收者名称
     private String mobile_no;  //接受者手机号码
     private String msg;  //信息模板
     private String comments;  //备注
     private String source_from;  //source_from
    
    // Constructors

    /** default constructor */
    public MsgSet() {
    }
    
    public String getSet_id() {
		return set_id;
	}

	public void setSet_id(String set_id) {
		this.set_id = set_id;
	}

	public String getSet_type() {
		return set_type;
	}

	public void setSet_type(String set_type) {
		this.set_type = set_type;
	}

	public String getReceiver() {
		return receiver;
	}

	public void setReceiver(String receiver) {
		this.receiver = receiver;
	}

	public String getMobile_no() {
		return mobile_no;
	}

	public void setMobile_no(String mobile_no) {
		this.mobile_no = mobile_no;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	public String getSource_from() {
		return source_from;
	}

	public void setSource_from(String source_from) {
		this.source_from = source_from;
	}
    
}