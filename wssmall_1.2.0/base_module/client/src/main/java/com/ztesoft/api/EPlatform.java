package com.ztesoft.api;

public enum EPlatform {
    Linux("Linux","2G"),  
    Windows("Windows","2G"),  
    Solaris("Solaris","4G"),  
    Others("Others","1G");  
    
    private String osName;
    private String maxProcessMemory;
    EPlatform(String osName,String maxProcessMemory){  
        this.osName = osName;  
        this.maxProcessMemory = maxProcessMemory;
    }
	public String getOsName() {
		return osName;
	}
	public void setOsName(String osName) {
		this.osName = osName;
	}
	public String getMaxProcessMemory() {
		return maxProcessMemory;
	}
	public void setMaxProcessMemory(String maxProcessMemory) {
		this.maxProcessMemory = maxProcessMemory;
	}  
}
