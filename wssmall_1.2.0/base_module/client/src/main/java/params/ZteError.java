package params;

/**
 * 
 * @author wui
 *
 */
public class ZteError {

	
	String type_code;  //BUSS业务异常
	String error_code;
	String error_msg;
	String error_cause;
	String is_write_exception;//是否写异常单  1 写入  0  不写入
	public ZteError(String error_code,String error_msg){
		this.error_code =error_code;
		this.error_msg =error_msg;
	}
	
	
	public ZteError(String error_code,String error_msg,String error_cause){
		this.error_code =error_code;
		this.error_msg =error_msg;
		this.error_cause =error_cause;
	}
	public ZteError(String error_code,String error_msg,String error_cause,String is_write_exception){
		this.error_code =error_code;
		this.error_msg =error_msg;
		this.error_cause =error_cause;
		this.is_write_exception = is_write_exception;
	}
	
	public String getType_code() {
		return type_code;
	}

	public void setType_code(String type_code) {
		this.type_code = type_code;
	}


	public String getError_code() {
		return error_code;
	}


	public void setError_code(String error_code) {
		this.error_code = error_code;
	}


	public String getError_msg() {
		return error_msg;
	}


	public void setError_msg(String error_msg) {
		this.error_msg = error_msg;
	}


	public String getError_cause() {
		return error_cause;
	}


	public void setError_cause(String error_cause) {
		this.error_cause = error_cause;
	}


	public String getIs_write_exception() {
		return is_write_exception;
	}


	public void setIs_write_exception(String is_write_exception) {
		this.is_write_exception = is_write_exception;
	}
	
	
}
