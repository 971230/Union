package zte.params.template.resp;

import java.util.Map;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

import params.ZteResponse;
/**
 * 模板接入出参
 * @author xuefeng
 */
public class TemplateAccessConvertResp extends ZteResponse{
	
	@ZteSoftCommentAnnotationParam(name="报文出参结构",type="Map",isNecessary="Y",desc="模板出参结构")
	private Map<String, Object> infOutMap;
	
	@ZteSoftCommentAnnotationParam(name="模版转入对象以map方式返回",type="Map",isNecessary="Y",desc="模版转入对象以map方式返回")
	private Map<String,Object>  outObjMap;

	public Map<String, Object> getInfOutMap() {
		return infOutMap;
	}

	public void setInfOutMap(Map<String, Object> infOutMap) {
		this.infOutMap = infOutMap;
	}

	public Map<String, Object> getOutObjMap() {
		return outObjMap;
	}

	public void setOutObjMap(Map<String, Object> outObjMap) {
		this.outObjMap = outObjMap;
	}

	
	
}
