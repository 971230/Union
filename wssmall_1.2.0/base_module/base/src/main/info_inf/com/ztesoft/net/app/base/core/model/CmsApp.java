package com.ztesoft.net.app.base.core.model;

import java.io.Serializable;

public class CmsApp implements Serializable {
	private String app_version;
	private String app_version_url;
	private String user_id;
	private String app_img_path;
	private String app_id;
	private String app_name;
	private String description;
	private String outer_app_code;
	private String app_package;
	
	public String getApp_version() {
		return app_version;
	}
	public void setApp_version(String app_version) {
		this.app_version = app_version;
	}
	public String getApp_version_url() {
		return app_version_url;
	}
	public void setApp_version_url(String app_version_url) {
		this.app_version_url = app_version_url;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getApp_img_path() {
		return app_img_path;
	}
	public void setApp_img_path(String app_img_path) {
		this.app_img_path = app_img_path;
	}
	public String getApp_id() {
		return app_id;
	}
	public void setApp_id(String app_id) {
		this.app_id = app_id;
	}
	public String getApp_name() {
		return app_name;
	}
	public void setApp_name(String app_name) {
		this.app_name = app_name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getOuter_app_code() {
		return outer_app_code;
	}
	public void setOuter_app_code(String outer_app_code) {
		this.outer_app_code = outer_app_code;
	}
	public String getApp_package() {
		return app_package;
	}
	public void setApp_package(String app_package) {
		this.app_package = app_package;
	}

}
