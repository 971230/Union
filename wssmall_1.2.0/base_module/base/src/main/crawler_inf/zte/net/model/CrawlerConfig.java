package zte.net.model;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

/**
 * 爬虫配置实体类
 */
public class CrawlerConfig implements Serializable {
	
	private static final long serialVersionUID = 1L;
	
	@ZteSoftCommentAnnotationParam(name="主键",type="String",isNecessary="Y",desc="主键")
	private String cfg_id; 
	@ZteSoftCommentAnnotationParam(name="爬取内容的目标地址",type="String",isNecessary="Y",desc="爬取内容的目标地址")
	private String address; 
	@ZteSoftCommentAnnotationParam(name="爬取内容的目标端口",type="String",isNecessary="Y",desc="爬取内容的目标端口")
	private String port; 
	@ZteSoftCommentAnnotationParam(name="是否走代理",type="String",isNecessary="Y",desc="N 默认不走代理; Y走代理")
	private String proxy_type; 
	@ZteSoftCommentAnnotationParam(name="代理地址",type="String",isNecessary="Y",desc="代理地址")
	private String proxy_address; 
	@ZteSoftCommentAnnotationParam(name="代理端口",type="String",isNecessary="Y",desc="代理端口")
	private String proxy_port; 
	@ZteSoftCommentAnnotationParam(name="代理用户名",type="String",isNecessary="Y",desc="代理用户名")
	private String proxy_user; 
	@ZteSoftCommentAnnotationParam(name="代理密码",type="String",isNecessary="Y",desc="代理密码")
	private String proxy_pwd; 
	@ZteSoftCommentAnnotationParam(name="是否开启单号匹配校验",type="String",isNecessary="Y",desc="Y 开启校验； N 不校验")
	private String is_out_id_check;

	public String getCfg_id() {
		return cfg_id;
	}
	public void setCfg_id(String cfg_id) {
		this.cfg_id = cfg_id;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getProxy_type() {
		return proxy_type;
	}
	public void setProxy_type(String proxy_type) {
		this.proxy_type = proxy_type;
	}
	public String getProxy_address() {
		return proxy_address;
	}
	public void setProxy_address(String proxy_address) {
		this.proxy_address = proxy_address;
	}
	public String getProxy_port() {
		return proxy_port;
	}
	public void setProxy_port(String proxy_port) {
		this.proxy_port = proxy_port;
	}
	public String getProxy_user() {
		return proxy_user;
	}
	public void setProxy_user(String proxy_user) {
		this.proxy_user = proxy_user;
	}
	public String getProxy_pwd() {
		return proxy_pwd;
	}
	public void setProxy_pwd(String proxy_pwd) {
		this.proxy_pwd = proxy_pwd;
	}
	public String getIs_out_id_check() {
		return is_out_id_check;
	}
	public void setIs_out_id_check(String is_out_id_check) {
		this.is_out_id_check = is_out_id_check;
	}
}

