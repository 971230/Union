package com.ztesoft.net.mall.core.model;

import com.ztesoft.net.framework.database.NotDbField;

/**
 * 购物车实体
 * @author kingapex
 *2010-3-23下午03:34:04
 */
public class Cart {
	
	private String cart_id;
	private String product_id;
	private Integer num;
	private Double weight;
	private String session_id;
	private Integer itemtype;
	private String name;
	private Double price;
	private String addon; //附件字串
	private String member_lv_id;
	private String spec_id;
	
	private String page_from;//来源页面,区分是团购，还是秒杀
	
	public String getMember_lv_id() {
		return member_lv_id;
	}
	public void setMember_lv_id(String member_lv_id) {
		this.member_lv_id = member_lv_id;
	}
	public String getCart_id() {
		return cart_id;
	}
	public void setCart_id(String cartId) {
		cart_id = cartId;
	}
	public String getProduct_id() {
		return product_id;
	}
	public void setProduct_id(String productId) {
		product_id = productId;
	}
	public Integer getNum() {
		return num;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	public String getSession_id() {
		return session_id;
	}
	public void setSession_id(String sessionId) {
		session_id = sessionId;
	}
	public Integer getItemtype() {
		return itemtype;
	}
	public void setItemtype(Integer itemtype) {
		this.itemtype = itemtype;
	}
	public Double getWeight() {
		return weight;
	}
	public void setWeight(Double weight) {
		this.weight = weight;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getAddon() {
		return addon;
	}
	public void setAddon(String addon) {
		this.addon = addon;
	}
	
	@NotDbField
	public String getPage_from() {
		return page_from;
	}
	@NotDbField
	public void setPage_from(String page_from) {
		this.page_from = page_from;
	}
	public String getSpec_id() {
		return spec_id;
	}
	public void setSpec_id(String spec_id) {
		this.spec_id = spec_id;
	}
	
	
}
