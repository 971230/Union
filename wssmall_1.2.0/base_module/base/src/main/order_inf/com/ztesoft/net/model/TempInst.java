package com.ztesoft.net.model;

import com.ztesoft.net.framework.database.NotDbField;


/**
 * 模板实例实体
 * @author hu.yi
 * @date 2013.9.26
 */
public class TempInst implements java.io.Serializable {
	
	
	private String temp_inst_id;
	private String source_from;				//主键
	private String temp_cols;				//模板列对应的列及列名
	private String temp_cols2;				//模板列对应的列及列名
	private String temp_notes;				//模板注释
	private String temp_contents;			//模板样板数据对应的列及列名
	private String temp_name;				//模板名
	
	private String[] titles;				//格式化后的模板列对应的列及列名
	private String[] contents;				//格式化后的模板样板数据对应的列及列名
	private String[] dupicates;				//不能重复的字段
	
	private String goods_id;
	
	public String getTemp_cols() {
		return temp_cols;
	}
	public void setTemp_cols(String temp_cols) {
		this.temp_cols = temp_cols;
	}
	public String getTemp_notes() {
		return temp_notes;
	}
	public void setTemp_notes(String temp_notes) {
		this.temp_notes = temp_notes;
	}
	public String getTemp_inst_id() {
		return temp_inst_id;
	}
	public void setTemp_inst_id(String temp_inst_id) {
		this.temp_inst_id = temp_inst_id;
	}
	public String getSource_from() {
		return source_from;
	}
	public void setSource_from(String source_from) {
		this.source_from = source_from;
	}
	public String getTemp_contents() {
		return temp_contents;
	}
	public void setTemp_contents(String temp_contents) {
		this.temp_contents = temp_contents;
	}
	public String getTemp_name() {
		return temp_name;
	}
	public void setTemp_name(String temp_name) {
		this.temp_name = temp_name;
	}
	@NotDbField
	public String[] getTitles() {
		return titles;
	}
	@NotDbField
	public void setTitles(String[] titles) {
		this.titles = titles;
	}
	@NotDbField
	public String[] getContents() {
		return contents;
	}
	@NotDbField
	public void setContents(String[] contents) {
		this.contents = contents;
	}
	@NotDbField
	public String[] getDupicates() {
		return dupicates;
	}
	@NotDbField
	public void setDupicates(String[] dupicates) {
		this.dupicates = dupicates;
	}
	public String getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(String goods_id) {
		this.goods_id = goods_id;
	}
	public String getTemp_cols2() {
		return temp_cols2;
	}
	public void setTemp_cols2(String temp_cols2) {
		this.temp_cols2 = temp_cols2;
	}
	
}