package com.ztesoft.net.mall.core.model;

public class DealLog {
	
	private String deal_order_id;
	private Integer deal_count;
	private String create_date;
	private String status_date;
	private String deal_result;
	private String type_code;
	private String comments;
	
	public String getDeal_order_id() {
		return deal_order_id;
	}
	public void setDeal_order_id(String deal_order_id) {
		this.deal_order_id = deal_order_id;
	}
	public Integer getDeal_count() {
		return deal_count;
	}
	public void setDeal_count(Integer deal_count) {
		this.deal_count = deal_count;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	public String getStatus_date() {
		return status_date;
	}
	public void setStatus_date(String status_date) {
		this.status_date = status_date;
	}
	public String getDeal_result() {
		return deal_result;
	}
	public void setDeal_result(String deal_result) {
		this.deal_result = deal_result;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public String getType_code() {
		return type_code;
	}
	public void setType_code(String type_code) {
		this.type_code = type_code;
	}

}
