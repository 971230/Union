package com.ztesoft.net.mall.core.model;

import java.util.List;


/**
 * 配货单
 * @author kingapex
 *
 */
public class Allocation {
	private String orderid;
	private String shipDepotId; //发货仓库id
	private List<AllocationItem> itemList;
	public String getOrderid() {
		return orderid;
	}
	public void setOrderid(String orderid) {
		this.orderid = orderid;
	}
	public String getShipDepotId() {
		return shipDepotId;
	}
	public void setShipDepotId(String shipDepotId) {
		this.shipDepotId = shipDepotId;
	}
	public List<AllocationItem> getItemList() {
		return itemList;
	}
	public void setItemList(List<AllocationItem> itemList) {
		this.itemList = itemList;
	}
	
	
}
