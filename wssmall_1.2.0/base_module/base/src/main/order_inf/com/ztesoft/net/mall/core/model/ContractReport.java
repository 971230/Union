package com.ztesoft.net.mall.core.model;


/**
 * 合约机实体
 * 
 * @author wui 2010-4-6上午11:11:27
 */
public class ContractReport implements java.io.Serializable {
	// 合约名称

	private String offer_name;
	//冗余字段，方便统计处理
	private String lan_name;
	private String username;
	private Integer succ_count;
	private Integer fail_count;
	private Integer all_count;
	private String userid;
	private String lan_id;
	private String order_id;
	//合约计划名称
	private String plan_name;
	
	public String getOffer_name() {
		return offer_name;
	}
	public void setOffer_name(String offer_name) {
		this.offer_name = offer_name;
	}
	public String getPlan_name() {
		return plan_name;
	}
	public void setPlan_name(String plan_name) {
		this.plan_name = plan_name;
	}
	public String getLan_name() {
		return lan_name;
	}
	public void setLan_name(String lan_name) {
		this.lan_name = lan_name;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public Integer getSucc_count() {
		return succ_count;
	}
	public void setSucc_count(Integer succ_count) {
		this.succ_count = succ_count;
	}
	public Integer getFail_count() {
		return fail_count;
	}
	public void setFail_count(Integer fail_count) {
		this.fail_count = fail_count;
	}
	public Integer getAll_count() {
		return all_count;
	}
	public void setAll_count(Integer all_count) {
		this.all_count = all_count;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getLan_id() {
		return lan_id;
	}
	public void setLan_id(String lan_id) {
		this.lan_id = lan_id;
	}
	public String getOrder_id() {
		return order_id;
	}
	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}
	

}