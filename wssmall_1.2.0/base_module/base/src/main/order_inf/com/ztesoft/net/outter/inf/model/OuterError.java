package com.ztesoft.net.outter.inf.model;

import java.io.Serializable;

public class OuterError implements Serializable {

	private String outer_pagkage_id;
	private String outer_sku_id;
	private String outer_iid;
	private String goods_name;
	private String order_from;
	private String tid;
	private String create_time;
	private String error_type;
	private String deal_flag = "0";
	private String deal_num;
	
	public OuterError() {
	}
	public OuterError(String outer_pagkage_id, String outer_sku_id,
			String outer_iid, String goods_name, String order_from, String tid,
			String create_time, String error_type) {
		this.outer_pagkage_id = outer_pagkage_id;
		this.outer_sku_id = outer_sku_id;
		this.outer_iid = outer_iid;
		this.goods_name = goods_name;
		this.order_from = order_from;
		this.tid = tid;
		this.create_time = create_time;
		this.error_type = error_type;
	}
	public String getOuter_pagkage_id() {
		return outer_pagkage_id;
	}
	public void setOuter_pagkage_id(String outer_pagkage_id) {
		this.outer_pagkage_id = outer_pagkage_id;
	}
	public String getOuter_sku_id() {
		return outer_sku_id;
	}
	public void setOuter_sku_id(String outer_sku_id) {
		this.outer_sku_id = outer_sku_id;
	}
	public String getOuter_iid() {
		return outer_iid;
	}
	public void setOuter_iid(String outer_iid) {
		this.outer_iid = outer_iid;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public String getOrder_from() {
		return order_from;
	}
	public void setOrder_from(String order_from) {
		this.order_from = order_from;
	}
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getError_type() {
		return error_type;
	}
	public void setError_type(String error_type) {
		this.error_type = error_type;
	}
	public String getDeal_flag() {
		return deal_flag;
	}
	public void setDeal_flag(String deal_flag) {
		this.deal_flag = deal_flag;
	}
	public String getDeal_num() {
		return deal_num;
	}
	public void setDeal_num(String deal_num) {
		this.deal_num = deal_num;
	}
	
}
