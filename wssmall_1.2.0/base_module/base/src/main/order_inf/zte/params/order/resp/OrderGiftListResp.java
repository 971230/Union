package zte.params.order.resp;

import java.util.List;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

import params.ZteResponse;

public class OrderGiftListResp extends ZteResponse {

	@ZteSoftCommentAnnotationParam(name="赠品列表",type="String",isNecessary="Y",desc="赠品列表")
	private List giftList;
	@ZteSoftCommentAnnotationParam(name="优惠活动列表",type="String",isNecessary="Y",desc="优惠活动列表")
	private List pmtList;

	public List getGiftList() {
		return giftList;
	}

	public void setGiftList(List giftList) {
		this.giftList = giftList;
	}

	public List getPmtList() {
		return pmtList;
	}

	public void setPmtList(List pmtList) {
		this.pmtList = pmtList;
	}
	
}
