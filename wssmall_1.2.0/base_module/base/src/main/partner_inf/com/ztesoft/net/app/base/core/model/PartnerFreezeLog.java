package com.ztesoft.net.app.base.core.model;

public class PartnerFreezeLog implements java.io.Serializable {
	
	private String partner_id ;
	private String log_id;
	private String op_time;
	private String op_type;
	private Integer old_amount;
	private Integer new_amount;
	private String comments;
	private Integer amount;
	private String userid;
	public String getPartner_id() {
		return partner_id;
	}
	public void setPartner_id(String partner_id) {
		this.partner_id = partner_id;
	}
	public String getLog_id() {
		return log_id;
	}
	public void setLog_id(String log_id) {
		this.log_id = log_id;
	}
	public String getOp_time() {
		return op_time;
	}
	public void setOp_time(String op_time) {
		this.op_time = op_time;
	}
	public String getOp_type() {
		return op_type;
	}
	public void setOp_type(String op_type) {
		this.op_type = op_type;
	}
	public Integer getOld_amount() {
		return old_amount;
	}
	public void setOld_amount(Integer old_amount) {
		this.old_amount = old_amount;
	}
	public Integer getNew_amount() {
		return new_amount;
	}
	public void setNew_amount(Integer new_amount) {
		this.new_amount = new_amount;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public Integer getAmount() {
		return amount;
	}
	public void setAmount(Integer amount) {
		this.amount = amount;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	
}
