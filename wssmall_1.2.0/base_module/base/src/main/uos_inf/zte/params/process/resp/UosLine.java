package zte.params.process.resp;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

/**
 * @author Reason.Yea
 * @version 创建时间：2014-9-26
 */
public class UosLine implements java.io.Serializable {
	@ZteSoftCommentAnnotationParam(name = "线条ID", type = "String", isNecessary = "N", desc = "")
	private String id;
	@ZteSoftCommentAnnotationParam(name = "起点ActivityDefineID,实例数据时为实例id", type = "String", isNecessary = "N", desc = "")
	private String from;
	@ZteSoftCommentAnnotationParam(name = "终点ActivityDefineID,实例数据时为实例id", type = "String", isNecessary = "N", desc = "")
	private String to;
	@ZteSoftCommentAnnotationParam(name = "运行状态", type = "String", isNecessary = "N", desc = "0:正在运行；1:已运行；-1:未运行")
	private String runState;

	public String getRunState() {
		return runState;
	}

	public void setRunState(String runState) {
		this.runState = runState;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

}
