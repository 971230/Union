package com.ztesoft.net.app.base.core.model;

import com.ztesoft.net.framework.database.NotDbField;
import com.ztesoft.net.framework.model.Image;

import java.util.List;

/**
 * 数据日志实体
 * 
 * @author kingapex 2010-10-19下午03:29:02
 */
public class DataLog {
	
	private String id;
	private String content;
	private String url;
	private String pics;
	private String sitename;
	private String domain;
	private String logtype;
	private String optype;
	private String dateline;
	private String userid;
	private String siteid;
	private List<Image> picList; // 图片实体列表，由pics字串转换而来
	
	
	

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getPics() {
		return pics;
	}

	public void setPics(String pics) {
		this.pics = pics;
	}

	public String getSitename() {
		return sitename;
	}

	public void setSitename(String sitename) {
		this.sitename = sitename;
	}

	public String getLogtype() {
		return logtype;
	}

	public void setLogtype(String logtype) {
		this.logtype = logtype;
	}

	public String getOptype() {
		return optype;
	}

	public void setOptype(String optype) {
		this.optype = optype;
	}

	public String getDateline() {
		return dateline;
	}

	public void setDateline(String dateline) {
		this.dateline = dateline;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getSiteid() {
		return siteid;
	}

	public void setSiteid(String siteid) {
		this.siteid = siteid;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}
	
	@NotDbField
	public List<Image> getPicList() {
		return picList;
	}

	public void setPicList(List<Image> picList) {
		this.picList = picList;
	}
	
}
