package zte.net.common.annontion.context.request;

import java.io.Serializable;

import params.ZteBusiRequest;

/**
 * <pre>
 * 功能说明：
 * </pre>
 * 
 * @author
 * @version 1.0
 */
public class RequestBeanDefinition<T extends ZteBusiRequest> implements Serializable {

	String service_name;

	String primary_keys;
	
	String depency_keys;
	
	String table_name;

	String service_desc;
	
	String store_type;
	
	String cache_store;

	String query_store;
	
	String asy_save;
	
	String asy_query;
	
	Class<T> beanClass;
	
	String table_archive;
	
	String source_from;
	
	String table_type;
	
	public String getService_name() {
		return service_name;
	}

	public void setService_name(String service_name) {
		this.service_name = service_name;
	}

	public String getPrimary_keys() {
		return primary_keys;
	}

	public void setPrimary_keys(String primary_keys) {
		this.primary_keys = primary_keys;
	}

	public String getTable_name() {
		return table_name;
	}

	public void setTable_name(String table_name) {
		this.table_name = table_name;
	}

	public String getService_desc() {
		return service_desc;
	}

	public void setService_desc(String service_desc) {
		this.service_desc = service_desc;
	}

	public String getCache_store() {
		return cache_store;
	}

	public void setCache_store(String cache_store) {
		this.cache_store = cache_store;
	}

	public String getQuery_store() {
		return query_store;
	}

	public void setQuery_store(String query_store) {
		this.query_store = query_store;
	}

	public String getAsy_save() {
		return asy_save;
	}

	public void setAsy_save(String asy_save) {
		this.asy_save = asy_save;
	}

	public String getAsy_query() {
		return asy_query;
	}

	public void setAsy_query(String asy_query) {
		this.asy_query = asy_query;
	}

	public Class<T> getBeanClass() {
		return beanClass;
	}

	public void setBeanClass(Class<T> beanClass) {
		this.beanClass = beanClass;
	}

	public String getStore_type() {
		return store_type;
	}

	public void setStore_type(String store_type) {
		this.store_type = store_type;
	}

	public String getDepency_keys() {
		return depency_keys;
	}

	public void setDepency_keys(String depency_keys) {
		this.depency_keys = depency_keys;
	}

	public String getTable_archive() {
		return table_archive;
	}

	public void setTable_archive(String table_archive) {
		this.table_archive = table_archive;
	}

	public String getSource_from() {
		return source_from;
	}

	public void setSource_from(String source_from) {
		this.source_from = source_from;
	}

	public String getTable_type() {
		return table_type;
	}

	public void setTable_type(String table_type) {
		this.table_type = table_type;
	}

}
