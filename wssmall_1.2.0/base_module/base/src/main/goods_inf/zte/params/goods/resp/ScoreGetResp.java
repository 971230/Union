package zte.params.goods.resp;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

import params.ZteResponse;

public class ScoreGetResp extends ZteResponse {
	@ZteSoftCommentAnnotationParam(name="评论数",type="Integer",isNecessary="Y",desc="comm_num：商品评论数。")
	private Integer comm_num;//评论数
	@ZteSoftCommentAnnotationParam(name="商品评分",type="Double",isNecessary="Y",desc="score：商品评分。")
	private Double score;//评分
	
	public Integer getComm_num() {
		return comm_num;
	}
	public void setComm_num(Integer comm_num) {
		this.comm_num = comm_num;
	}
	public Double getScore() {
		return score;
	}
	public void setScore(Double score) {
		this.score = score;
	}
}
