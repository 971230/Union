public class Favorite implements java.io.Serializable {
	private String favorite_id;
	private String member_id;
	private String goods_id;
	private String favorite_type;
	
	public String getFavorite_id() {
		return favorite_id;
	}

	public void setFavorite_id(String favoriteId) {
		favorite_id = favoriteId;
	}

	public String getMember_id() {
		return member_id;
	}

	public void setMember_id(String memberid) {
		member_id = memberid;
	}

	public String getGoods_id() {
		return goods_id;
	}

	public void setGoods_id(String goodsId) {
		goods_id = goodsId;
	}

	public String getFavorite_type() {
		return favorite_type;
	}

	public void setFavorite_type(String favorite_type) {
		this.favorite_type = favorite_type;
	}
	
}
