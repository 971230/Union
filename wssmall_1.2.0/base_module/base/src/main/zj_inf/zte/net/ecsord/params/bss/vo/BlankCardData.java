package zte.net.ecsord.params.bss.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;
/**
 * 
 * 获取白卡数据返回参数
 * @author yan.pj
 *
 */
public class BlankCardData implements Serializable {

	@ZteSoftCommentAnnotationParam(name="IMSI值",type="String",isNecessary="Y",desc="imsi：IMSI值")
	private String imsi;
	
	@ZteSoftCommentAnnotationParam(name="SCRIPTSEQ值",type="String",isNecessary="Y",desc="scriptseq：SCRIPTSEQ值")
	private String scriptseq;
	
	@ZteSoftCommentAnnotationParam(name="发起方业务流水",type="String",isNecessary="Y",desc="proc_id：发起方业务流水")
	private String proc_id;

	public String getImsi() {
		return imsi;
	}

	public void setImsi(String imsi) {
		this.imsi = imsi;
	}

	public String getScriptseq() {
		return scriptseq;
	}

	public void setScriptseq(String scriptseq) {
		this.scriptseq = scriptseq;
	}

	public String getProc_id() {
		return proc_id;
	}

	public void setProc_id(String proc_id) {
		this.proc_id = proc_id;
	}

	
}
