package zte.net.ecsord.params.bss.resp;
import zte.net.ecsord.params.base.resp.ZteBusiResponse;
import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

/**
 * 商城收费、退款成功触发接口 请求对象
 */
public class BSSAccountOfficialSubResp extends ZteBusiResponse {

	@ZteSoftCommentAnnotationParam(name="调用结果",type="String",isNecessary="Y",desc="验证结果[非空，00000表示成功]")
	private String code;

	@ZteSoftCommentAnnotationParam(name="结果描述",type="String",isNecessary="Y",desc="结果描述")
	private String msg;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}


	
	

}
