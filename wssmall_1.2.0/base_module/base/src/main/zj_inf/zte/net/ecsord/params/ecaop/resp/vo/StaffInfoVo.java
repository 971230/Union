package zte.net.ecsord.params.ecaop.resp.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class StaffInfoVo  implements Serializable{
	private static final long serialVersionUID = 1L;

	@ZteSoftCommentAnnotationParam(name = "员工姓名", type = "String", isNecessary = "Y", desc = "staffName:员工姓名")
	private String staffName;
	
	@ZteSoftCommentAnnotationParam(name = "员工联系电话", type = "String", isNecessary = "Y", desc = "staffNumber:员工联系电话")
	private String staffNumber;
	
	@ZteSoftCommentAnnotationParam(name = "员工性别", type = "Integer", isNecessary = "Y", desc = "staffSex:员工性别")
	private String staffSex;
	
	@ZteSoftCommentAnnotationParam(name = "证件类型", type = "String", isNecessary = "Y", desc = "staffPsptType:证件类型")
	private String staffPsptType;
	
	@ZteSoftCommentAnnotationParam(name = "证件号码", type = "String", isNecessary = "Y", desc = "staffPsptId:证件号码")
	private String staffPsptId;

	public String getStaffName() {
		return staffName;
	}

	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}

	public String getStaffNumber() {
		return staffNumber;
	}

	public void setStaffNumber(String staffNumber) {
		this.staffNumber = staffNumber;
	}

	public String getStaffSex() {
		return staffSex;
	}

	public void setStaffSex(String staffSex) {
		this.staffSex = staffSex;
	}

	public String getStaffPsptType() {
		return staffPsptType;
	}

	public void setStaffPsptType(String staffPsptType) {
		this.staffPsptType = staffPsptType;
	}

	public String getStaffPsptId() {
		return staffPsptId;
	}

	public void setStaffPsptId(String staffPsptId) {
		this.staffPsptId = staffPsptId;
	}

}
