package zte.net.ecsord.params.ems.vo;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class EmsWaybillResp {
	@ZteSoftCommentAnnotationParam(name = "物流运单号", type = "String", isNecessary = "Y", desc = "物流运单号")
	private String mailNo;
	@ZteSoftCommentAnnotationParam(name = "接收结果", type = "String", isNecessary = "Y", desc = "接收结果:T-成功,F-失败")
	private String success;
	@ZteSoftCommentAnnotationParam(name = "错误信息", type = "String", isNecessary = "N", desc = "错误信息")
	private String errorMsg;
	@ZteSoftCommentAnnotationParam(name = "错误代码", type = "String", isNecessary = "N", desc = "错误代码")
	private String errorCode;

	public String getMailNo() {
		return mailNo;
	}

	public void setMailNo(String mailNo) {
		this.mailNo = mailNo;
	}

	public String getSuccess() {
		return success;
	}

	public void setSuccess(String success) {
		this.success = success;
	}

	public String getErrorMsg() {
		return errorMsg;
	}

	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}

	public String getErrorCode() {
		return errorCode;
	}

	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}
}
