package zte.net.ecsord.params.zb.vo.orderattr;

/**
 * @author ZX
 * @version 2016-01-11
 * @see 附加产品可选包
 * 
 */
public class SubPackage {
	
	private String subPackageCode; // 可选包编码
	private String subPackageName; // 可选包名称
	private String subElementCode; // 元素编码
	private String subElementType; // 元素类型
	private String subElementName; // 元素名称
	
	public String getSubPackageCode() {
		return subPackageCode;
	}
	public void setSubPackageCode(String subPackageCode) {
		this.subPackageCode = subPackageCode;
	}
	public String getSubPackageName() {
		return subPackageName;
	}
	public void setSubPackageName(String subPackageName) {
		this.subPackageName = subPackageName;
	}
	public String getSubElementCode() {
		return subElementCode;
	}
	public void setSubElementCode(String subElementCode) {
		this.subElementCode = subElementCode;
	}
	public String getSubElementType() {
		return subElementType;
	}
	public void setSubElementType(String subElementType) {
		this.subElementType = subElementType;
	}
	public String getSubElementName() {
		return subElementName;
	}
	public void setSubElementName(String subElementName) {
		this.subElementName = subElementName;
	}
	
}
