package zte.net.ecsord.params.ecaop.req.vo;

import java.util.List;

import org.apache.commons.lang3.StringUtils;

/**
 * @author zengxianlian
 * @version BSS
 *
 */
public class BSSProComItemInfoVo {

	private String ProComItemType;
	private String ProComItemID;
	private List<BSSProComItemAttrVo> ProComItemAttr;
	
	public String getProComItemType() {
		if (StringUtils.isBlank(ProComItemType)) return null;
		return ProComItemType;
	}
	public void setProComItemType(String proComItemType) {
		this.ProComItemType = proComItemType;
	}
	public String getProComItemID() {
		if (StringUtils.isBlank(ProComItemID)) return null;
		return ProComItemID;
	}
	public void setProComItemID(String proComItemID) {
		this.ProComItemID = proComItemID;
	}
	public List<BSSProComItemAttrVo> getProComItemAttr() {
		if (ProComItemAttr==null || ProComItemAttr.size() <= 0) return null;
		return ProComItemAttr;
	}
	public void setProComItemAttr(List<BSSProComItemAttrVo> proComItemAttr) {
		this.ProComItemAttr = proComItemAttr;
	}

	
}
