package zte.net.ecsord.params.ecaop.resp;

import params.ZteResponse;

public class CommAopSubmitRsp extends ZteResponse {
	/**
	 * 能力开放平台返回码
	 */
	private String res_code;
	
	/**
	 * 能力开放平台返回信息
	 */
	private String res_message;
	
	/**
	 * AOP接口返回信息
	 */
	private CommAopSubmitResultMsg resultMsg;

	public String getRes_code() {
		return res_code;
	}

	public void setRes_code(String res_code) {
		this.res_code = res_code;
	}

	public String getRes_message() {
		return res_message;
	}

	public void setRes_message(String res_message) {
		this.res_message = res_message;
	}

	public CommAopSubmitResultMsg getResultMsg() {
		return resultMsg;
	}

	public void setResultMsg(CommAopSubmitResultMsg resultMsg) {
		this.resultMsg = resultMsg;
	}
}
