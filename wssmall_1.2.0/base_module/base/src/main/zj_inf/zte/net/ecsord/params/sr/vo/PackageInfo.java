package zte.net.ecsord.params.sr.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class PackageInfo implements Serializable {

	@ZteSoftCommentAnnotationParam(name="packageId",type="String",isNecessary="Y",desc="packageId：包编号")
	private String packageId;
	
	@ZteSoftCommentAnnotationParam(name="elementId",type="String",isNecessary="Y",desc="elementId：元素编号")
	private String elementId;

	public String getPackageId() {
		return packageId==null?"":packageId;
	}

	public void setPackageId(String packageId) {
		this.packageId = packageId;
	}

	public String getElementId() {
		return elementId==null?"":elementId;
	}

	public void setElementId(String elementId) {
		this.elementId = elementId;
	}
}
