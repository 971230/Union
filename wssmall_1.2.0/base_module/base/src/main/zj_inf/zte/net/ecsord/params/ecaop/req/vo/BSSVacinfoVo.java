package zte.net.ecsord.params.ecaop.req.vo;

import org.apache.commons.lang3.StringUtils;

/**
 * @author zengxianlian
 * @version BSS
 *
 */
public class BSSVacinfoVo {
	private String Party_id;//YSP标识
	private String SpProductId;//Y增值业务产品标识
	private String orderTime;//Y订购时间yyyyMMddhhmmss
	public String getParty_id() {
		if (StringUtils.isBlank(Party_id)) return null;
		return Party_id;
	}
	public void setParty_id(String party_id) {
		this.Party_id = party_id;
	}
	public String getSpProductId() {
		if (StringUtils.isBlank(SpProductId)) return null;
		return SpProductId;
	}
	public void setSpProductId(String spProductId) {
		this.SpProductId = spProductId;
	}
	public String getOrderTime() {
		if (StringUtils.isBlank(orderTime)) return null;
		return orderTime;
	}
	public void setOrderTime(String orderTime) {
		this.orderTime = orderTime;
	}
}
