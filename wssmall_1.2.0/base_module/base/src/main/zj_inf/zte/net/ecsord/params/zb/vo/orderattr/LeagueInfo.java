package zte.net.ecsord.params.zb.vo.orderattr;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class LeagueInfo implements Serializable {
	@ZteSoftCommentAnnotationParam(name="加盟商ID",type="String",isNecessary="N",desc="LeagueID：加盟商ID")
	private String LeagueID;
	@ZteSoftCommentAnnotationParam(name="加盟商名称",type="String",isNecessary="N",desc="LeagueName：加盟商名称")
	private String LeagueName;
	@ZteSoftCommentAnnotationParam(name="挂靠加盟商",type="String",isNecessary="N",desc="HigherLeagueID：挂靠加盟商")
	private String HigherLeagueID;
	@ZteSoftCommentAnnotationParam(name="挂靠加盟商名称",type="String",isNecessary="N",desc="HigherLeagueName：挂靠加盟商名称")
	private String HigherLeagueName;
	public String getLeagueID() {
		return LeagueID;
	}
	public void setLeagueID(String leagueID) {
		LeagueID = leagueID;
	}
	public String getLeagueName() {
		return LeagueName;
	}
	public void setLeagueName(String leagueName) {
		LeagueName = leagueName;
	}
	public String getHigherLeagueID() {
		return HigherLeagueID;
	}
	public void setHigherLeagueID(String higherLeagueID) {
		HigherLeagueID = higherLeagueID;
	}
	public String getHigherLeagueName() {
		return HigherLeagueName;
	}
	public void setHigherLeagueName(String higherLeagueName) {
		HigherLeagueName = higherLeagueName;
	}

	
}
