package zte.net.ecsord.params.sr.resp;

import params.ZteResponse;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class SimulationResponse  extends ZteResponse {
	
	@ZteSoftCommentAnnotationParam(name = "message", type = "String", isNecessary = "Y", desc = "message：返回各种提示信息，包括成功失败信息")
	private String message;
	
	@ZteSoftCommentAnnotationParam(name = "code", type = "String", isNecessary = "Y", desc = "code：开户成功失败")
	private String code;
	
	@ZteSoftCommentAnnotationParam(name = "activeNo", type = "String", isNecessary = "Y", desc = "activeNo：流水号")
	private String activeNo;

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getActiveNo() {
		return activeNo;
	}

	public void setActiveNo(String activeNo) {
		this.activeNo = activeNo;
	}	
}
