package zte.net.ecsord.params.ecaop.resp.vo;

public class AddrInfos {
	private String standard_addr_id ; //标准地址ID
	
	private String standard_addr_name; //标准地址名称
	
	private String post_code ; //局向编码

	public String getStandard_addr_id() {
		return standard_addr_id;
	}

	public void setStandard_addr_id(String standard_addr_id) {
		this.standard_addr_id = standard_addr_id;
	}

	public String getStandard_addr_name() {
		return standard_addr_name;
	}

	public void setStandard_addr_name(String standard_addr_name) {
		this.standard_addr_name = standard_addr_name;
	}

	public String getPost_code() {
		return post_code;
	}

	public void setPost_code(String post_code) {
		this.post_code = post_code;
	}
	
	
}
