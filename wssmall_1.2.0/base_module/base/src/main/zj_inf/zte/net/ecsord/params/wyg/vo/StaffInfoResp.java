package zte.net.ecsord.params.wyg.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class StaffInfoResp implements Serializable{
	@ZteSoftCommentAnnotationParam(name="员工编码",type="String",isNecessary="Y",desc="StaffCode：员工编码")
	private String StaffCode;
	@ZteSoftCommentAnnotationParam(name="结果编码",type="String",isNecessary="Y",desc="ResultCode：结果编码")
	private String ResultCode;
	@ZteSoftCommentAnnotationParam(name="结果描述",type="String",isNecessary="Y",desc="ResultMsg：结果描述")
	private String ResultMsg;
	public String getStaffCode() {
		return StaffCode;
	}
	public void setStaffCode(String staffCode) {
		StaffCode = staffCode;
	}
	public String getResultCode() {
		return ResultCode;
	}
	public void setResultCode(String resultCode) {
		ResultCode = resultCode;
	}
	public String getResultMsg() {
		return ResultMsg;
	}
	public void setResultMsg(String resultMsg) {
		ResultMsg = resultMsg;
	}

}
