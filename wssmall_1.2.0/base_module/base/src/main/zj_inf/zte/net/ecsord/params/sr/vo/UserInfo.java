package zte.net.ecsord.params.sr.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class UserInfo implements Serializable {

	@ZteSoftCommentAnnotationParam(name="userName",type="String",isNecessary="Y",desc="userName：登陆cbss用户名")
	private String userName;
	
	@ZteSoftCommentAnnotationParam(name="province",type="String",isNecessary="Y",desc="province：登陆cbss选择省份，如：广东为51")
	private String province;

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

}
