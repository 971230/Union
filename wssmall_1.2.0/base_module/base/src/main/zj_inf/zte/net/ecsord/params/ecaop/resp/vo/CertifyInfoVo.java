package zte.net.ecsord.params.ecaop.resp.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class CertifyInfoVo implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -3962999083129231574L;
	@ZteSoftCommentAnnotationParam(name = "责任人/使用人标识", type = "String", isNecessary = "N", desc = "CertifyTag")
	private String CertifyTag;
	@ZteSoftCommentAnnotationParam(name = "证件类型", type = "String", isNecessary = "N", desc = "CertType")
	private String CertType ;
	@ZteSoftCommentAnnotationParam(name = "证件号码", type = "String", isNecessary = "N", desc = "CertNum")
	private String CertNum ;
	@ZteSoftCommentAnnotationParam(name = "姓名", type = "String", isNecessary = "N", desc = "Name")
	private String Name ;
	@ZteSoftCommentAnnotationParam(name = "证件地址", type = "String", isNecessary = "N", desc = "CertAddr")
	private String CertAddr ;
	public String getCertifyTag() {
		return CertifyTag;
	}
	public void setCertifyTag(String certifyTag) {
		CertifyTag = certifyTag;
	}
	public String getCertType() {
		return CertType;
	}
	public void setCertType(String certType) {
		CertType = certType;
	}
	public String getCertNum() {
		return CertNum;
	}
	public void setCertNum(String certNum) {
		CertNum = certNum;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getCertAddr() {
		return CertAddr;
	}
	public void setCertAddr(String certAddr) {
		CertAddr = certAddr;
	}
	
}
