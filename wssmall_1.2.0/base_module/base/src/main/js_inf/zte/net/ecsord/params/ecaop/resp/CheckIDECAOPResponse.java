package zte.net.ecsord.params.ecaop.resp;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

import params.ZteResponse;

public class CheckIDECAOPResponse  extends ZteResponse {
	@ZteSoftCommentAnnotationParam(name="身份证号码",type="String",isNecessary="Y",desc="certId：身份证号码")
	private String certId;
	
	@ZteSoftCommentAnnotationParam(name="身份证姓名",type="String",isNecessary="Y",desc="certName：(提供证件号码的真实姓名)")
	private String certName;
	
	@ZteSoftCommentAnnotationParam(name="JPEG照片编码， base64字符串编码",type="String",isNecessary="Y",desc="photo：JPEG照片编码， base64字符串编码")
	private String photo;
	
	@ZteSoftCommentAnnotationParam(name="性别",type="String",isNecessary="Y",desc="sex:固定长度1位， M：男， F：女")
	private String sex;
	
	@ZteSoftCommentAnnotationParam(name="生日",type="String",isNecessary="Y",desc="birthday:生日")
	private String birthday;	
	
	@ZteSoftCommentAnnotationParam(name="民族",type="String",isNecessary="Y",desc="nation:民族")
	private String nation;
	
	@ZteSoftCommentAnnotationParam(name="证件地址",type="String",isNecessary="Y",desc="addr:证件地址")
	private String addr;
	
	@ZteSoftCommentAnnotationParam(name="证件失效日期",type="String",isNecessary="Y",desc="exp:证件失效日期")
	private String exp;	
	
	@ZteSoftCommentAnnotationParam(name="发证机关",type="String",isNecessary="Y",desc="issue:发证机关")
	private String issue;

	public String getCertId() {
		return certId;
	}

	public void setCertId(String certId) {
		this.certId = certId;
	}

	public String getCertName() {
		return certName;
	}

	public void setCertName(String certName) {
		this.certName = certName;
	}

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getBirthday() {
		return birthday;
	}

	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}

	public String getNation() {
		return nation;
	}

	public void setNation(String nation) {
		this.nation = nation;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public String getExp() {
		return exp;
	}

	public void setExp(String exp) {
		this.exp = exp;
	}

	public String getIssue() {
		return issue;
	}

	public void setIssue(String issue) {
		this.issue = issue;
	}
	
	
}
