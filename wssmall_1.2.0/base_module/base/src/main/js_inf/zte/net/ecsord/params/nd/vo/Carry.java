package zte.net.ecsord.params.nd.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class Carry implements Serializable {

	

	/**
	 * 
	 */
	private static final long serialVersionUID = -5892740181495550097L;

	@ZteSoftCommentAnnotationParam(name="收货人姓名",type="String",isNecessary="Y",desc="consigneeName：收货人姓名")
	private String consigneeName;
	
	@ZteSoftCommentAnnotationParam(name="收货邮编",type="String",isNecessary="N",desc="receiveZip：收货邮编")
	private String receiveZip;
	
	@ZteSoftCommentAnnotationParam(name="收货人电话",type="String",isNecessary="N",desc="receiveTelNum：收货人电话")
	private String receiveTelNum;	
	
	@ZteSoftCommentAnnotationParam(name="收货人手机",type="String",isNecessary="N",desc="receiveMobileNum：收货人手机")
	private String receiveMobileNum;	
	
	@ZteSoftCommentAnnotationParam(name="收货地址",type="String",isNecessary="Y",desc="receiveAddress：收货地址")
	private String receiveAddress;	
	
	@ZteSoftCommentAnnotationParam(name="配送方式",type="String",isNecessary="Y",desc="carryMode：配送方式")
	private String carryMode;

	public String getConsigneeName() {
		return consigneeName;
	}

	public void setConsigneeName(String consigneeName) {
		this.consigneeName = consigneeName;
	}

	public String getReceiveZip() {
		return receiveZip;
	}

	public void setReceiveZip(String receiveZip) {
		this.receiveZip = receiveZip;
	}

	public String getReceiveTelNum() {
		return receiveTelNum;
	}

	public void setReceiveTelNum(String receiveTelNum) {
		this.receiveTelNum = receiveTelNum;
	}

	public String getReceiveMobileNum() {
		return receiveMobileNum;
	}

	public void setReceiveMobileNum(String receiveMobileNum) {
		this.receiveMobileNum = receiveMobileNum;
	}

	public String getReceiveAddress() {
		return receiveAddress;
	}

	public void setReceiveAddress(String receiveAddress) {
		this.receiveAddress = receiveAddress;
	}

	public String getCarryMode() {
		return carryMode;
	}

	public void setCarryMode(String carryMode) {
		this.carryMode = carryMode;
	}
	
}
