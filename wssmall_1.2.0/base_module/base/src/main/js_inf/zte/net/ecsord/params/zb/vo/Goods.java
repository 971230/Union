package zte.net.ecsord.params.zb.vo;

import java.io.Serializable;
import java.util.List;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class Goods implements Serializable {
	@ZteSoftCommentAnnotationParam(name = "货品类型", type = "String", isNecessary = "Y", desc = "GoodsType：货品类型")
	private String GoodsType;

	@ZteSoftCommentAnnotationParam(name = "货品机型编码", type = "String", isNecessary = "N", desc = "GoodsTypeId：货品机型编码")
	private String GoodsTypeId;

	@ZteSoftCommentAnnotationParam(name = "货品关键编码", type = "String", isNecessary = "N", desc = "GoodsKeyCode：货品关键编码")
	private String GoodsKeyCode;

	@ZteSoftCommentAnnotationParam(name = "货品关键信息", type = "String", isNecessary = "N", desc = "GoodsKeyName:货品关键信息")
	private String GoodsKeyName;

	@ZteSoftCommentAnnotationParam(name = "货品数量", type = "int", isNecessary = "N", desc = "GoodsNum:货品数量")
	private int GoodsNum;

	@ZteSoftCommentAnnotationParam(name = "货品费用", type = "int", isNecessary = "N", desc = "GoodsFee:货品费用")
	private int GoodsFee;

	@ZteSoftCommentAnnotationParam(name = "供货商信息", type = "Supp", isNecessary = "N", desc = "SuppInfo:供货商信息")
	private List<Supp> SuppInfo;

	public String getGoodsType() {
		return GoodsType;
	}

	public void setGoodsType(String goodsType) {
		GoodsType = goodsType;
	}

	public String getGoodsTypeId() {
		return GoodsTypeId;
	}

	public void setGoodsTypeId(String goodsTypeId) {
		GoodsTypeId = goodsTypeId;
	}

	public String getGoodsKeyCode() {

		return GoodsKeyCode;
	}

	public void setGoodsKeyCode(String goodsKeyCode) {
		GoodsKeyCode = goodsKeyCode;
	}

	public String getGoodsKeyName() {
		return GoodsKeyName;
	}

	public void setGoodsKeyName(String goodsKeyName) {
		GoodsKeyName = goodsKeyName;
	}

	public int getGoodsNum() {
		return GoodsNum;
	}

	public void setGoodsNum(int goodsNum) {
		GoodsNum = goodsNum;
	}

	public int getGoodsFee() {
		return GoodsFee;
	}

	public void setGoodsFee(int goodsFee) {
		GoodsFee = goodsFee;
	}

	public List<Supp> getSuppInfo() {
		return SuppInfo;
	}

	public void setSuppInfo(List<Supp> suppInfo) {
		SuppInfo = suppInfo;
	}

}
