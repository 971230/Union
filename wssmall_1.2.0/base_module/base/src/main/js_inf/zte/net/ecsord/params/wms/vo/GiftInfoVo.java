package zte.net.ecsord.params.wms.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;


/**
 * 同步订单信息到WMS系统
 * 礼品信息对象
 */
public class GiftInfoVo implements Serializable{
	
	private static final long serialVersionUID = -6937152527303504657L;
	
	@ZteSoftCommentAnnotationParam(name="交互编码",type="String",isNecessary="Y",desc="交互编码（交互编码）")
	private String giftDetailID;
	@ZteSoftCommentAnnotationParam(name="礼品SKU",type="String",isNecessary="Y",desc="礼品SKU（明细编码，用于拣货）")
	private String giftCode;
	@ZteSoftCommentAnnotationParam(name="礼品编码",type="String",isNecessary="Y",desc="礼品编码")
	private String giftID;
	@ZteSoftCommentAnnotationParam(name="礼品名称",type="String",isNecessary="N",desc="礼品名称")
	private String giftName;
	@ZteSoftCommentAnnotationParam(name="礼品类型",type="String",isNecessary="N",desc="礼品类型（可根据业务拓展）[01：卡、02：实物]")
	private String giftType;
	@ZteSoftCommentAnnotationParam(name="礼品数量",type="String",isNecessary="Y",desc="礼品数量")
	private Integer giftNum;
	@ZteSoftCommentAnnotationParam(name="礼品说明",type="String",isNecessary="N",desc="礼品说明")
	private String giftDesc;
	@ZteSoftCommentAnnotationParam(name="是否加工",type="String",isNecessary="N",desc="是否加工[0：不加工、1:加工]")
	private String whetherProcessing;
	@ZteSoftCommentAnnotationParam(name="加工类型",type="String",isNecessary="N",desc="加工类型[01：手机壳刻字、99：其他]")
	private String processingType;
	@ZteSoftCommentAnnotationParam(name="加工内容",type="String",isNecessary="N",desc="加工内容")
	private String processingContent;

	public String getGiftDetailID() {
		return giftDetailID;
	}

	public void setGiftDetailID(String giftDetailID) {
		this.giftDetailID = giftDetailID;
	}

	public String getGiftCode() {
		return giftCode;
	}

	public void setGiftCode(String giftCode) {
		this.giftCode = giftCode;
	}

	public String getGiftID() {
		return giftID;
	}

	public void setGiftID(String giftID) {
		this.giftID = giftID;
	}

	public String getGiftName() {
		return giftName;
	}

	public void setGiftName(String giftName) {
		this.giftName = giftName;
	}

	public String getGiftType() {
		return giftType;
	}

	public void setGiftType(String giftType) {
		this.giftType = giftType;
	}

	public Integer getGiftNum() {
		return giftNum;
	}

	public void setGiftNum(Integer giftNum) {
		this.giftNum = giftNum;
	}

	public String getGiftDesc() {
		return giftDesc;
	}

	public void setGiftDesc(String giftDesc) {
		this.giftDesc = giftDesc;
	}

	public String getWhetherProcessing() {
		return whetherProcessing;
	}

	public void setWhetherProcessing(String whetherProcessing) {
		this.whetherProcessing = whetherProcessing;
	}

	public String getProcessingType() {
		return processingType;
	}

	public void setProcessingType(String processingType) {
		this.processingType = processingType;
	}

	public String getProcessingContent() {
		return processingContent;
	}

	public void setProcessingContent(String processingContent) {
		this.processingContent = processingContent;
	}
}
