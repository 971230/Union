package com.ztesoft.net.model;

import java.io.Serializable;

public class EsearchExpInstSolution implements Serializable {

	private static final long serialVersionUID = 1L;
	
	//解决方案id
	private String solution_id;
	//规则rule，方案plan,URL,DEFAULT,DOC,SQL
	private String solution_type;
	//解决方案配置值
	private String solution_value;
	//解决方案描述
	private String solution_desc;
	//解决方案名称
	private String solution_name;
	//是否删除规则执行日志 Y 是 N 否
	private String is_delete_rule_log;
	//执行方案或者规则所需的Fact
	private String hander_fact;
	//是否支持批处理 Y 是 N 否
	private String is_batch_process;
	
	public String getSolution_id() {
		return solution_id;
	}
	public void setSolution_id(String solution_id) {
		this.solution_id = solution_id;
	}
	public String getSolution_type() {
		return solution_type;
	}
	public void setSolution_type(String solution_type) {
		this.solution_type = solution_type;
	}
	public String getSolution_value() {
		return solution_value;
	}
	public void setSolution_value(String solution_value) {
		this.solution_value = solution_value;
	}
	public String getSolution_desc() {
		return solution_desc;
	}
	public void setSolution_desc(String solution_desc) {
		this.solution_desc = solution_desc;
	}
	public String getSolution_name() {
		return solution_name;
	}
	public void setSolution_name(String solution_name) {
		this.solution_name = solution_name;
	}
	public String getIs_delete_rule_log() {
		return is_delete_rule_log;
	}
	public void setIs_delete_rule_log(String is_delete_rule_log) {
		this.is_delete_rule_log = is_delete_rule_log;
	}
	public String getHander_fact() {
		return hander_fact;
	}
	public void setHander_fact(String hander_fact) {
		this.hander_fact = hander_fact;
	}
	public String getIs_batch_process() {
		return is_batch_process;
	}
	public void setIs_batch_process(String is_batch_process) {
		this.is_batch_process = is_batch_process;
	}
}