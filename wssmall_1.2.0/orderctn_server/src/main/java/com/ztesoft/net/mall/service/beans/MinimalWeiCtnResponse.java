package com.ztesoft.net.mall.service.beans;

public class MinimalWeiCtnResponse {

	private String RESP_CODE;//8888-失败、0000- 成功
	
	private String RESP_DESC;//应答描述
	
	private String TRADE_ID;//预受理单号
	
	private String SERIAL_NUMBER;//号码

	public String getRESP_CODE() {
		return RESP_CODE;
	}

	public void setRESP_CODE(String rESP_CODE) {
		RESP_CODE = rESP_CODE;
	}

	public String getRESP_DESC() {
		return RESP_DESC;
	}

	public void setRESP_DESC(String rESP_DESC) {
		RESP_DESC = rESP_DESC;
	}

	public String getTRADE_ID() {
		return TRADE_ID;
	}

	public void setTRADE_ID(String tRADE_ID) {
		TRADE_ID = tRADE_ID;
	}

	public String getSERIAL_NUMBER() {
		return SERIAL_NUMBER;
	}

	public void setSERIAL_NUMBER(String sERIAL_NUMBER) {
		SERIAL_NUMBER = sERIAL_NUMBER;
	}

	@Override
	public String toString() {
		return "{RESP_CODE=\"" + RESP_CODE + "\", RESP_DESC=\""
				+ RESP_DESC + "\", TRADE_ID=\"" + TRADE_ID + "\", SERIAL_NUMBER=\""
				+ SERIAL_NUMBER + "\"}";
	}
	
	
}
