package zte.params.orderctn.vo;

 /**
 * @Description http返回信息节点实体
 * @author  zhangJun
 * @date    2015-4-10
 * @version 1.0
 */
public class HttpRespInfo implements java.io.Serializable {
	
	private String process_code="";
	private String sysfunc_id="";
	//private String response_time;
	private String request_source="";
	private String response_seq="";
	private String request_type="";
	
	
	
	private String resp_result;
	private String resp_code;
	private String resp_desc;
	private String content="";
	
	
	
	public HttpRespInfo() {
		
	}
	public HttpRespInfo( String resp_result,
			String resp_code, String resp_desc) {
		
		this.resp_result = resp_result;
		this.resp_code = resp_code;
		this.resp_desc = resp_desc;
	}
	public String getProcess_code() {
		return process_code;
	}
	public void setProcess_code(String process_code) {
		this.process_code = process_code;
	}
	public String getSysfunc_id() {
		return sysfunc_id;
	}
	public void setSysfunc_id(String sysfunc_id) {
		this.sysfunc_id = sysfunc_id;
	}
	public String getResponse_seq() {
		return response_seq;
	}
	public void setResponse_seq(String response_seq) {
		this.response_seq = response_seq;
	}
	
	public String getRequest_type() {
		return request_type;
	}
	public void setRequest_type(String request_type) {
		this.request_type = request_type;
	}
	public String getResp_result() {
		return resp_result;
	}
	public void setResp_result(String resp_result) {
		this.resp_result = resp_result;
	}
	public String getResp_code() {
		return resp_code;
	}
	public void setResp_code(String resp_code) {
		this.resp_code = resp_code;
	}
	public String getResp_desc() {
		return resp_desc;
	}
	public void setResp_desc(String resp_desc) {
		this.resp_desc = resp_desc;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getRequest_source() {
		return request_source;
	}
	public void setRequest_source(String request_source) {
		this.request_source = request_source;
	}
	
	
	
}