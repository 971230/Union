package com.ztesoft.net.ecsord.params.ecaop.resp;

import java.util.List;




import com.ztesoft.net.ecsord.params.ecaop.vo.TradeQueryPayInfoVO;
import com.ztesoft.net.ecsord.params.ecaop.vo.TradeQueryRefundInfoVO;

import params.ZteResponse;

public class TradeQueryResp extends ZteResponse{

	private String data;
	private String resp_code;
	private String resp_msg;
	private String resultCode;
	private String resultDesc;
	private String tradeType;
	private String outTradeNo;
	private List<TradeQueryPayInfoVO> payInfo; 
	private List<TradeQueryRefundInfoVO> refundInfo;
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getResp_code() {
		return resp_code;
	}
	public void setResp_code(String resp_code) {
		this.resp_code = resp_code;
	}
	public String getResp_msg() {
		return resp_msg;
	}
	public void setResp_msg(String resp_msg) {
		this.resp_msg = resp_msg;
	}
	public String getResultCode() {
		return resultCode;
	}
	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}
	public String getResultDesc() {
		return resultDesc;
	}
	public void setResultDesc(String resultDesc) {
		this.resultDesc = resultDesc;
	}
	public String getTradeType() {
		return tradeType;
	}
	public void setTradeType(String tradeType) {
		this.tradeType = tradeType;
	}
	public String getOutTradeNo() {
		return outTradeNo;
	}
	public void setOutTradeNo(String outTradeNo) {
		this.outTradeNo = outTradeNo;
	}
	public List<TradeQueryPayInfoVO> getPayInfo() {
		return payInfo;
	}
	public void setPayInfo(List<TradeQueryPayInfoVO> payInfo) {
		this.payInfo = payInfo;
	}
	public List<TradeQueryRefundInfoVO> getRefundInfo() {
		return refundInfo;
	}
	public void setRefundInfo(List<TradeQueryRefundInfoVO> refundInfo) {
		this.refundInfo = refundInfo;
	}
}
