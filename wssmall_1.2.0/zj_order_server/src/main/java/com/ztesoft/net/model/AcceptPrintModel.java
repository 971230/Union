package com.ztesoft.net.model;

import java.io.Serializable;

public class AcceptPrintModel implements Serializable{
	private String id;
	private String receipt_code;//受理单打印模板编码
	private String receipt_name;//受理单打印模板名称
	private String receipt_mode;//受理单打印模式(0：套打；1：白打)
	private String apply_type;//缺省值为1，按1做条件查询
	private String receipt_url;//模板URL地址
	private String source_from;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getReceipt_code() {
		return receipt_code;
	}
	public void setReceipt_code(String receipt_code) {
		this.receipt_code = receipt_code;
	}
	public String getReceipt_name() {
		return receipt_name;
	}
	public void setReceipt_name(String receipt_name) {
		this.receipt_name = receipt_name;
	}
	public String getReceipt_mode() {
		return receipt_mode;
	}
	public void setReceipt_mode(String receipt_mode) {
		this.receipt_mode = receipt_mode;
	}
	public String getApply_type() {
		return apply_type;
	}
	public void setApply_type(String apply_type) {
		this.apply_type = apply_type;
	}
	public String getReceipt_url() {
		return receipt_url;
	}
	public void setReceipt_url(String receipt_url) {
		this.receipt_url = receipt_url;
	}
	public String getSource_from() {
		return source_from;
	}
	public void setSource_from(String source_from) {
		this.source_from = source_from;
	}
	

}
