package com.ztesoft.net.ecsord.params.ecaop.vo;

public class FtthObjectInfo {
	
	private String object_id ;//物品元素ID
	
	private String object_name ; //物品名称
	
	private String object_type ; //物品大类型
	
	private String object_sub_type ; //物品小类
	
	private String is_obj_manage ; //是否调用物管的判测
	
	private String is_obj_sn ; //验证码或者串号的选择  1验证码0串号
	
	private String fee_subject ; //买断型费用的科目
	
	private String deposit_subject ;//租用型费用的科目
	
	private String onu_mode ; //光猫工作模式  B-桥接型，R-路由型(路由型的光猫默认也支持桥接型)
	
	private String ftth_object_typ ; //物品型号

	public String getObject_id() {
		return object_id;
	}

	public void setObject_id(String object_id) {
		this.object_id = object_id;
	}

	public String getObject_name() {
		return object_name;
	}

	public void setObject_name(String object_name) {
		this.object_name = object_name;
	}

	public String getObject_type() {
		return object_type;
	}

	public void setObject_type(String object_type) {
		this.object_type = object_type;
	}

	public String getObject_sub_type() {
		return object_sub_type;
	}

	public void setObject_sub_type(String object_sub_type) {
		this.object_sub_type = object_sub_type;
	}

	public String getIs_obj_manage() {
		return is_obj_manage;
	}

	public void setIs_obj_manage(String is_obj_manage) {
		this.is_obj_manage = is_obj_manage;
	}

	public String getIs_obj_sn() {
		return is_obj_sn;
	}

	public void setIs_obj_sn(String is_obj_sn) {
		this.is_obj_sn = is_obj_sn;
	}

	public String getFee_subject() {
		return fee_subject;
	}

	public void setFee_subject(String fee_subject) {
		this.fee_subject = fee_subject;
	}

	public String getDeposit_subject() {
		return deposit_subject;
	}

	public void setDeposit_subject(String deposit_subject) {
		this.deposit_subject = deposit_subject;
	}

	public String getOnu_mode() {
		return onu_mode;
	}

	public void setOnu_mode(String onu_mode) {
		this.onu_mode = onu_mode;
	}

	public String getFtth_object_typ() {
		return ftth_object_typ;
	}

	public void setFtth_object_typ(String ftth_object_typ) {
		this.ftth_object_typ = ftth_object_typ;
	}
	
	
}
