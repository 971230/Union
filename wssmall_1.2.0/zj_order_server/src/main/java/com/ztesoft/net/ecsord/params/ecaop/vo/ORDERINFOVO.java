package com.ztesoft.net.ecsord.params.ecaop.vo;

import java.util.List;

public class ORDERINFOVO {

	private String ACCEPT_NO;
	private String USER_NAME;
	private String EPARCHY_CODE;
	private String PRODUCT_TYPE_CODE;
	private List<PROGRESSINFOVO> PROGRESS_INFO;
	
	public String getACCEPT_NO() {
		return ACCEPT_NO;
	}
	public void setACCEPT_NO(String aCCEPT_NO) {
		ACCEPT_NO = aCCEPT_NO;
	}
	public String getUSER_NAME() {
		return USER_NAME;
	}
	public void setUSER_NAME(String uSER_NAME) {
		USER_NAME = uSER_NAME;
	}
	public String getEPARCHY_CODE() {
		return EPARCHY_CODE;
	}
	public void setEPARCHY_CODE(String ePARCHY_CODE) {
		EPARCHY_CODE = ePARCHY_CODE;
	}
	public String getPRODUCT_TYPE_CODE() {
		return PRODUCT_TYPE_CODE;
	}
	public void setPRODUCT_TYPE_CODE(String pRODUCT_TYPE_CODE) {
		PRODUCT_TYPE_CODE = pRODUCT_TYPE_CODE;
	}
	public List<PROGRESSINFOVO> getPROGRESS_INFO() {
		return PROGRESS_INFO;
	}
	public void setPROGRESS_INFO(List<PROGRESSINFOVO> pROGRESS_INFO) {
		PROGRESS_INFO = pROGRESS_INFO;
	}
	
}
