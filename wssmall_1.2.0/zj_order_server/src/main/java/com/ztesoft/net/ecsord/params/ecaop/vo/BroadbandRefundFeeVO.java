package com.ztesoft.net.ecsord.params.ecaop.vo;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class BroadbandRefundFeeVO {

	@ZteSoftCommentAnnotationParam(name = "收费项科目", type = "String", isNecessary = "Y", desc = "收费项科目")
	private String feeCategory;
	@ZteSoftCommentAnnotationParam(name = "原始金额(分)", type = "Number", isNecessary = "Y", desc = "原始金额(分)")
	private String origFee;
	@ZteSoftCommentAnnotationParam(name = "应收费用(分)", type = "Number", isNecessary = "Y", desc = "应收费用(分)")
	private String fee;
	@ZteSoftCommentAnnotationParam(name = "数量", type = "Number", isNecessary = "Y", desc = "数量")
	private String cnt;
	
	public String getFeeCategory() {
		return feeCategory;
	}
	public void setFeeCategory(String feeCategory) {
		this.feeCategory = feeCategory;
	}
	public String getOrigFee() {
		return origFee;
	}
	public void setOrigFee(String origFee) {
		this.origFee = origFee;
	}
	public String getFee() {
		return fee;
	}
	public void setFee(String fee) {
		this.fee = fee;
	}
	public String getCnt() {
		return cnt;
	}
	public void setCnt(String cnt) {
		this.cnt = cnt;
	}
	
	
}
