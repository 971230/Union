package com.ztesoft.net.ecsord.params.ecaop.vo;

import java.io.Serializable;

import com.ztesoft.net.annotation.ZteSoftCommentAnnotationParam;

public class OrderQueryInfo implements Serializable {

	@ZteSoftCommentAnnotationParam(name="contact_phone",type="String",isNecessary="N",desc="contact_phone：联系电话")
	private String contact_phone;
	
	@ZteSoftCommentAnnotationParam(name="ICCID",type="String",isNecessary="N",desc="ICCID：开通号码的iccid")
	private String ICCID;
	
	@ZteSoftCommentAnnotationParam(name="out_order_id",type="String",isNecessary="N",desc="out_order_id：外部单号")
	private String out_order_id;

	public String getContact_phone() {
		return contact_phone;
	}

	public void setContact_phone(String contact_phone) {
		this.contact_phone = contact_phone;
	}

	public String getICCID() {
		return ICCID;
	}

	public void setICCID(String iCCID) {
		ICCID = iCCID;
	}

	public String getOut_order_id() {
		return out_order_id;
	}

	public void setOut_order_id(String out_order_id) {
		this.out_order_id = out_order_id;
	}
}
