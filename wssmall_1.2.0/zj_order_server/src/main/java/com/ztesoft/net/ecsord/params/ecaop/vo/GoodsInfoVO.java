package com.ztesoft.net.ecsord.params.ecaop.vo;

import java.io.Serializable;
import java.util.List;


public class GoodsInfoVO implements Serializable{
	private static final long serialVersionUID = 1L;
	private String goods_id; 
	private String goods_name; 
	private float price; 
	private String simple_name; 
	private String type_id;
	private String cat_id;
	private List<ProdsInfoVO> prods_info ;
	
	public String getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(String goods_id) {
		this.goods_id = goods_id;
	}
	public String getGoods_name() {
		return goods_name;
	} 
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public String getSimple_name() {
		return simple_name;
	}
	public void setSimple_name(String simple_name) {
		this.simple_name = simple_name;
	}
	public String getType_id() {
		return type_id;
	}
	public void setType_id(String type_id) {
		this.type_id = type_id;
	}
	public String getCat_id() {
		return cat_id;
	}
	public void setCat_id(String cat_id) {
		this.cat_id = cat_id;
	}
	public List<ProdsInfoVO> getProds_info() {
		return prods_info;
	}
	public void setProds_info(List<ProdsInfoVO> prods_info) {
		this.prods_info = prods_info;
	}
}
