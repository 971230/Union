package com.ztesoft.net.outter.inf.model;

/**
 * 定时任务
 */
public class OutterTmplNew {

	private String tmpl_id;			//主键
	private String tmpl_code;		//任务编码(备用)
	private String tmpl_content;	//任务内容
	private String create_time;		//任务创建时间
	private String last_execute;	//上次执行时间
	private String next_execute;	//下一次执行时间
	private String data_end_time;	//接口数据结束时间点(上次同步时接口传送的时间)
	private Integer execute_min;	//执行间隔时间(分钟)
	private String result_msg;		//上次执行结果(备用)
	private String run_status;		//任务状态(00A：running；00R：closed)
	private String source_from;		//ECS
	
	public String getTmpl_id() {
		return tmpl_id;
	}
	public void setTmpl_id(String tmpl_id) {
		this.tmpl_id = tmpl_id;
	}
	public String getTmpl_code() {
		return tmpl_code;
	}
	public void setTmpl_code(String tmpl_code) {
		this.tmpl_code = tmpl_code;
	}
	public String getTmpl_content() {
		return tmpl_content;
	}
	public void setTmpl_content(String tmpl_content) {
		this.tmpl_content = tmpl_content;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getLast_execute() {
		return last_execute;
	}
	public void setLast_execute(String last_execute) {
		this.last_execute = last_execute;
	}
	public String getNext_execute() {
		return next_execute;
	}
	public void setNext_execute(String next_execute) {
		this.next_execute = next_execute;
	}
	public String getData_end_time() {
		return data_end_time;
	}
	public void setData_end_time(String data_end_time) {
		this.data_end_time = data_end_time;
	}
	public Integer getExecute_min() {
		return execute_min;
	}
	public void setExecute_min(Integer execute_min) {
		this.execute_min = execute_min;
	}
	public String getResult_msg() {
		return result_msg;
	}
	public void setResult_msg(String result_msg) {
		this.result_msg = result_msg;
	}
	public String getRun_status() {
		return run_status;
	}
	public void setRun_status(String run_status) {
		this.run_status = run_status;
	}
	public String getSource_from() {
		return source_from;
	}
	public void setSource_from(String source_from) {
		this.source_from = source_from;
	}
}
